export function getRetailPriceGhs(
  displayNetworkId: number,
  gb: number
): number | null {
  const g = Number(gb);
  if (!Number.isFinite(g) || g <= 0) return null;
  const key = Number.isInteger(g) ? g : g;
  const map =
    displayNetworkId === 1
      ? MTN_RETAIL_GHS
      : displayNetworkId === 2
        ? TELECEL_RETAIL_GHS
        : displayNetworkId === 3
          ? AIRTELTIGO_RETAIL_GHS
          : null;
  if (!map) return null;
  return map[key] ?? null;
}

export function buildFallbackPackagesList(): Array<{
  id: string;
  network: { id: number; name: NetworkName };
  dataAmount: string;
  validity: string;
  price: number;
  sharedBundle: number;
}> {
  const out: Array<{
    id: string;
    network: { id: number; name: NetworkName };
    dataAmount: string;
    validity: string;
    price: number;
    sharedBundle: number;
  }> = [];

  for (const net of NETWORKS) {
    const map =
      net.id === 1
        ? MTN_RETAIL_GHS
        : net.id === 2
          ? TELECEL_RETAIL_GHS
          : AIRTELTIGO_RETAIL_GHS;
    for (const [gbStr, price] of Object.entries(map)) {
      const gb = Number(gbStr);
      out.push({
        id: `${net.name.toLowerCase()}-${gb}gb`,
        network: { id: net.id, name: net.name },
        dataAmount: `${gb} GB`,
        validity: "30 days",
        price,
        sharedBundle: gb,
      });
    }
  }
  return out;
}
